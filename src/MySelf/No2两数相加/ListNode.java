package MySelf.No2两数相加;

//Definition for singly-linked list. 单链表的定义。
public class ListNode  {
    int val;
    ListNode next;

    //构造器
    ListNode() {
    }

    ListNode(int val) {
        this.val = val;
    }

    ListNode(int val, ListNode next) {
        this.val = val;
        this.next = next;
    }
}